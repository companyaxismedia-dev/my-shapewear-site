export function convertRangeToCm(range, decimal = false) {
    if (!range) return "";

    const parts = range.split("-").map((v) => Number(v.trim()));
    if (parts.length !== 2 || parts.some((n) => Number.isNaN(n))) return range;

    const [min, max] = parts;

    if (decimal) {
        const cmMin = +(min * 2.54).toFixed(1);
        const cmMax = +(max * 2.54).toFixed(1);
        return `${cmMin}-${cmMax}`;
    }

    const cmMin = Math.round(min * 2.54);
    const cmMax = Math.round(max * 2.54);
    return `${cmMin}-${cmMax}`;
}

